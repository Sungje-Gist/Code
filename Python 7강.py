 # + 연산자는 문자열과 문자열을 연결짓는다. *문자열은 숫자열과 다르다.
 # * 연산자는 문자열의 반복 횟수를 의미한다.
 # print는 여러가지 방법으로 여러 줄을 출력 할 수 있다.
 # escape sequence : \n (줄바꿈), \t (탭), \\(\ 출력), \"(" 출력), \'(' 출력)
 # type함수는 괄호안에 있는 자료의 자료형을 알고싶을때 사용한다.
print(type("안녕하세요")) # 괄호안의 자료형을 출력하고 싶다면 print함수를 사용 이때 첫번째 괄호에서 ""는 사용하지 않는다.
print(type(1308)) # 문자열의 경우 ""을 사용하는데 이때문에 자료형이 str로 인식되고, ""을 사용하지 않는 숫자의 경우 int or float형으로 인식된다.
 