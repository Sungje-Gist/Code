# 소수점이 있는 숫자(정수 < 실수)
# 존재하지 않는 수 = 허수(정수 < 실수 < 복소수)
# Python에서는 숫자의 나눗셈을 할때 float형인지 int형인지 크게 신경을 안써도 된다.(대부분 float형으로 반환)
# 연산자 : *, +, **(제곱), %, //(몫)
# print함수의 경우 숫자를 출력하기 위해서는 "" 가 필요없지만, 문자열의 출력을 위해선 ""가 필요하다.
print("기본적인 연산")
print(15, "+", 4, "=", 15+4)
print(15, "-", 4, "=", 15-4)
print(15, "*", 4, "=", 15*4)
print(15, "/", 4, "=", 15 / 4)